end

function base.arr_reverse(arr)
	local rs = {}
	local len = #arr
	for i = len, 1, -1 do
		rs[len - i + 1] = arr[i]
	end
	return rs
end

function base.to_hexarr(arr)
	local rs = {}
    local len = #arr
	for i = 1, len do
		rs[i] = string.format("%02X", arr[i])
	end
	return rs
end

