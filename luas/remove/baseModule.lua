	local idx
	while dataLen > 0 do
		idx = crc_h ~ (tab[pos] & 0xFF)
		crc_h = (crc_l ~ auchCRCHi[idx + 1]) & 0xFFFF
		crc_l = (auchCRCLo[idx + 1]) & 0xFFFF
