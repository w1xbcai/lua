end		

function base.int_to_4bytes(value)
	local rs = {};
	rs[1] = (value >> 24) & 0xFF
	rs[2] = (value >> 16) & 0xFF
	rs[3] = (value >> 8) & 0xFF
	rs[4] = (value) & 0xFF
	return rs
end

function base.int_to_2bytes(value)
